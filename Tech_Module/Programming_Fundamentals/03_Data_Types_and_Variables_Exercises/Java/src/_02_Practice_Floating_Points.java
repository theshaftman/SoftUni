import java.math.BigDecimal;

public class _02_Practice_Floating_Points {
    public static void main(String[] args) {
        BigDecimal decimalVarA = new BigDecimal("3.141592653589793238");
        Double doubleVar = 1.60217657;
        BigDecimal decimalVarB = new BigDecimal("7.8184261974584555216535342341");

        System.out.println(decimalVarA);
        System.out.println(doubleVar);
        System.out.println(decimalVarB);
    }
}